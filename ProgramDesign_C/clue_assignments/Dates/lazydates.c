//lazy dates.c

#include <stdio.h>

int main (void)
{
    int month1, day1, year1;
    int month2, day2, year2;

    printf("Enter the first date (mm/dd/yyyy)");
	scanf("%d*c%d*c%d", &month1, &day1, &year1);

    scanf();

	printf("Enter the second date (mm/dd/yyyy)");
	scanf("%d*c%d*c%d", &month2, &day2, &year2);

return 0;
}
